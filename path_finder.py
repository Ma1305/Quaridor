def is_path(position, board, pre_positions):
    pre_positions.append(position)
    availables = []
    if position - 17 < 0:
        return True
    elif position + 17 > 17*17:
        availables.append(position-17)
    else:
        availables.append(position + 17)
        availables.append(position - 17)
    if position % 17 == 0:
        availables.append(position-1)
    elif position % 17 == 1:
        availables.append(position+1)
    else:
        availables.append(position-1)
        availables.append(position+1)

    for pos in availables:
        if (pos + (pos - position)) not in pre_positions:
            if board[pos-1] == 0:
                if board[(pos+(pos-position))-1] < 0:
                    return True
                else:
                    if is_path((pos + (pos - position)), board, pre_positions):
                        return True
    return False

